package com.wso2telco.analytics.model;

public class Commission {

    private Double sp;
    private Double ads;
    private Double opco;

    public Double getSp() {
        return sp;
    }

    public void setSp(Double sp) {
        this.sp = sp;
    }

    public Double getAds() {
        return ads;
    }

    public void setAds(Double ads) {
        this.ads = ads;
    }

    public Double getOpco() {
        return opco;
    }

    public void setOpco(Double opco) {
        this.opco = opco;
    }
}


